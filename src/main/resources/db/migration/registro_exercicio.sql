CREATE TABLE IF NOT EXISTS registro_exercicios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    registro_treino_id BIGINT NOT NULL,
    exercicio_id BIGINT NOT NULL,
    treino_exercicio_id BIGINT,
    carga_real NUMERIC(10, 2),
    repeticoes_real INTEGER,
    concluido BOOLEAN NOT NULL,
    criado_em TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_registro_exercicios PRIMARY KEY (id),
    CONSTRAINT fk_registro_exercicios_registro_treino FOREIGN KEY (registro_treino_id) REFERENCES registros_treino (id),
    CONSTRAINT fk_registro_exercicios_exercicio FOREIGN KEY (exercicio_id) REFERENCES exercicios (id),
    CONSTRAINT fk_registro_exercicios_treino_exercicio FOREIGN KEY (treino_exercicio_id) REFERENCES treino_exercicios (id)
);

CREATE INDEX IF NOT EXISTS idx_registro_exercicios_registro
    ON registro_exercicios (registro_treino_id, id);

CREATE INDEX IF NOT EXISTS idx_registro_exercicios_exercicio
    ON registro_exercicios (exercicio_id);

CREATE UNIQUE INDEX IF NOT EXISTS uk_registro_exercicios_registro_treino_exercicio
    ON registro_exercicios (registro_treino_id, treino_exercicio_id)
    WHERE treino_exercicio_id IS NOT NULL;

